// Adding port normalization, in the server to  handling error and basic logging to  Node server makes  app run more consistently and easier to debug.
const normalizePort = val => {
  const port = parseInt(val, 10);

  if (isNaN(port)) {
    return val;
  }
  if (port >= 0) {
    return port;
  }
  return false;
};
